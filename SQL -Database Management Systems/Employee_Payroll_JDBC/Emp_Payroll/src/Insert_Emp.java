    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String eid, ename, dob, sex, desig;
        Double basic;
        eid = jTextField1.getText();
        ename = jTextField2.getText();
        dob = jTextField3.getText();
        sex = jTextField4.getText();
        desig = jTextField5.getText();
        try{ basic = Double.parseDouble(jTextField6.getText()); }
        catch(NumberFormatException e)
        { JOptionPane.showMessageDialog(this, "Basic pay field is empty!"); basic = 0.0; }
        
        try {
            Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "HR", "1234");
            String query = "Insert into emp_payroll (eid, ename, dob, sex, designation, basic) values('"+eid+"','"+ename+"','"+dob+"','"+sex+"','"+desig+"','"+basic+"')";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            
            query = "call cal_net_pay('"+eid+"','"+basic+"')";
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            
            JOptionPane.showMessageDialog(this, "Employee added and Net Pay calculated successfully!");
        } catch (SQLException ex) {
            Logger.getLogger(Insert_Emp.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "An Exception has been thrown. Insertion failed!");
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

